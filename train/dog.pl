Psy na znaczkach pocztowych
Pies w filatelistyce pojawił się obok pierwszego znaczka pocztowego w Anglii w 1840 roku pod postacią jaką dziś nazwalibyśmy całostką pocztową czyli tzw. koperty Mulread'ego[18].

Od tego czasu pies często gościł na znaczku pocztowym jako serie lub pojedyncze egzemplarze, przedstawiające poszczególne rasy lub jako motyw w sztuce. Często jest elementem kasownika okolicznościowego zarówno z okazji wystaw psów rasowych, jak też różnego typu imprez np. wyścigów na Alasce czy uczczenia rocznicy stulecia Kanadyjskiego Zarządu Hodowców Psów[19]. Pies jest znakiem zodiakalnym w kalendarzu chińskim, co w 1994 i 2006 roku skrzętnie wykorzystały poczty świata.

Pies na znaczku pocztowym corocznie jest emitowany w kilkunastu krajach. Kasowników również nie brakuje, prym wiodą Stany Zjednoczone, Niemcy i Francja. Polska emitowała serie z psami w następujących latach: 1963 (w tym znaczek, na którym znajduje się błąd w pisowni)[20] 1969, 1989, 2002[21] i 2006, z okazji Światowej Wystawy Psów Rasowych organizowanej po raz pierwszy w Polsce, w Poznaniu. Pierwszy polski znaczek z psem ukazał się w 1938 roku z okazji V Ogólnopolskiej Wystawy Filatelistycznej w Warszawie.

